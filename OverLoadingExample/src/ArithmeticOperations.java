
public class ArithmeticOperations {
	int addition (int a, int b) {
		int sum = a+b;
		return sum;
	}
	int addition (int p, int q, int r) { //method overloading and compile time polymorphism
		int sum = p+q+r;
		return sum;
	}
	double addition (double o, double p) {
		double sum = o+p;
		return sum;
	}
	

}
