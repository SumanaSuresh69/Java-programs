
public class RainbowColors {

	public static void main(String[] args) {
		System.out.println("program to illustrate number of colors in rainbow");
		String color = "Purple";
		switch (color) {
		case "Red":
			System.out.println("Rainbow has Red color");
			break;
		case "Blue":
			System.out.println("Rainbow has Blue color");
			break;
		case "Orange":
			System.out.println("Rainbow has Orange color");
			break;
		case "Yellow":
			System.out.println("Rainbow has Yellow color");
			break;
		case "Green":
			System.out.println("Rainbow has Green color");
			break;
		case "Indigo":
			System.out.println("Rainbow has Indigo color");
			break;
		case "Violet":
			System.out.println("Rainbow has Violet color");
			break;
		default:
			System.out.println("you have entered wrong color");
		}
		System.out.println("end of the program");

	}

}
